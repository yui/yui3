<p>This example demonstrates the use of <code>UA</code> to identify
the user's browser and to branch JavaScript logic based on what browser is
being used.  (<strong>Note:</strong> We strongly recommend using feature
detection rather than user-agent sniffing to fork code; only use this technique
where it is absolutely necessary to do so &mdash; for example, in cases where
browsers do not report their own capabilities accurately.)</p>
